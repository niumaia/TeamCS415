package librarysystem;

public class CopyStatus {
	
	private int statusId;
	private String statusDesc;
	

	public int getId() {
		return statusId;
	}

	public void setId(int id) {
		this.statusId = id;
	}
	
	public String getDesc() {
		return statusDesc;
	}

	public void setDesc(String desc) {
		this.statusDesc = desc;
	}
	
	
}
